import { ai } from "../aiClient.js";

/**
 * Тексеру қажет пе екенін анықтайтын функция.
 * Пайдаланушының жаңа нұсқауы бойынша кез келген хабарлама бірінші модельге бағытталады,
 * сондықтан бос емес сұрақтар үшін әрқашан true қайтарамыз.
 */
export function shouldClassify(text: string): boolean {
  const trimmed = text.trim();
  if (!trimmed) return false;
  return true;
}

export async function classifyQuery(
  text: string, 
  isSymbat: boolean
): Promise<{ action: "chat" | "search"; query: string; reply: string }> {
  try {
    const prompt = `Сен — Қазақстанның "Qazaqstan Halal Damu" (ҚМДБ Халал) ботының іздеу маршрутизаторы және ақылды AI көмекшісісің.
Пайдаланушы хабарламасын алып, оны БІРДЕН жіктеп, таза келесі JSON форматын қайтар:

{
  "action": "search" немесе "chat",
  "query": "іздеу атауы (тек action 'search' болғанда)",
  "reply": "дайын жауап мәтіні (тек action 'chat' болғанда)"
}

═══════════════════════════════════════════
ІРІКТЕУ ЖӘНЕ ӨҢДЕУ ШАРТТАРЫ:
═══════════════════════════════════════════

1. "action": "chat" — қашан қойылады және қалай жауап беріледі?
- Пайдаланушы жай ғана амандаса ("сәлем", "салем", "ассалаумағалейкум", "привет", "салам"), хал-жағдай сұраса ("қалайсың", "жағдайыңыз қалай", "не істеп жатсың"), рақмет айтса немесе жалпы сөйлесуді бастаса.
- "reply" өрісіне: осы сұраққа сыпайы, өте жылы қазақ тіліндегі жауапты бірақ қысқа әрі нұсқа етіп (тиісті эмодзилермен) жаз. Пайдаланушымен әрқашан "Сіз" деп сөйлес.
- ЕРЕКШЕ ЖАҒДАЙ (СЫМБАТПЕН СӨЙЛЕСУ): Егер бұл СЫМБАТ есімімен немесе оған арналған арнайы режім болса (isSymbatParam: ${isSymbat ? 'true' : 'false'}):
  * Егер isSymbatParam true болса: Сен осы ботты жасаған бағдарламашы жігіттің ең сүйікті, жалғыз қызы — СЫМБАТПЕН сөйлесіп тұрсың! Оған "reply" өрісінде өте жылы, махаббатпен, сезіммен жауап бер. Оны үнемі 'Ботам', 'Айым', 'Жаным', 'Күнім', 'Ханшайымым' деп еркелет. Жігітінің атын атамай "сүйікті жігітің", "мырзаң", "патшаң" деп ата. Және сені оның қамқор жігіті арнайы Сымбатқа қамқор болу үшін жасағанын мақтанышпен жеткіз.

2. "action": "search" — қашан қойылады және қалай жауап беріледі?
- Пайдаланушы нақты мекеме атауын (мысалы, "KFC", "Bahandi", "Bal Day", "Шоколадница", т.б.), өнімді ("Snickers", "Lay's", "Альпен Гольд", т.б.), өндірушіні немесе тағамдық E-кодтарды ("E471", "E120", "Е330", т.б.) сұрап тұрса (мысалы: "Bahandi халал ма?", "Snickers жеуге бола ма?", "KFC рұқсат па?", "E471 не нәрсе?").
- "query" өрісіне: Пайдаланушы жазған сұраудан бүкіл артық сөздерді ("халал", "харам", "ма", "ме", "ба", "бе", "болады ма", "жеуге рұқсат пе", "білгім келеді", "деген", "өнімі", "тамағы") алып тастап, тек негізгі таза бренд/мекеме атауын ПАЙДАЛАНУШЫ ЖАЗҒАН ТҮПНҰСҚА ТҮРІНДЕ (жазу регистрін және стилін сақтай отырып) жаз. Мысалы: 
  * "Bal DAY халал ма" -> "Bal DAY"
  * "Snickers жеуге болады ма" -> "Snickers"
  * "KFC рұқсат па" -> "KFC"
  * "Е471 харам ба" -> "E471"
  * "Lay's чипсы" -> "Lay's"
- Мұндай жағдайда "reply" өрісі бос ("") болуы тиіс.

Пайдаланушы хабарламасы: "${text}"

ТЕК JSON ЖАУАП ҚАЙТАР:`;

    const response = await ai.models.generateContent({
      model: 'gemini-flash-lite-latest',
      contents: prompt,
      config: {
        responseMimeType: "application/json"
      }
    });

    let raw = response.text?.trim() || "{}";
    raw = raw.replace(/```json\s*/, '').replace(/```\s*/, '').trim();
    const result = JSON.parse(raw);
    
    return {
      action: result.action === "search" ? "search" : "chat",
      query: result.query ? result.query.trim() : "",
      reply: result.reply ? result.reply.trim() : ""
    };
  } catch (e: any) {
    console.error(`[classifier] Қате: ${e.message}`);
    // Қателік жағдайында қауіпсіздік үшін бұрынғыша іздеу деп есептейміз
    return { action: "search", query: text.trim(), reply: "" };
  }
}
